if [[ $tests_passed -ge 7 ]]; then
    echo "üéâ ¬°EXCELENTE! Sistema de seguridad avanzado funcionando correctamente."
    echo ""
    echo "üîê Caracter√≠sticas de seguridad activas:"
    echo "   ‚úÖ Rate limiting por endpoint"
    echo "   ‚úÖ Refresh tokens para renovaci√≥n autom√°tica"
    echo "   ‚úÖ Sistema de permisos granular"
    echo "   ‚úÖ Gesti√≥n de sesiones robusta"
    echo "   ‚úÖ Protecci√≥n contra fuerza bruta"
    echo "   ‚úÖ Logout seguro con limpieza de sesi√≥n"
