# ===============================================
# TEST 3: VALIDACI√ìN DE PERMISOS
# ===============================================
echo "üîç TEST 3: Validaci√≥n de Permisos..."

if [[ -n "$access_token" ]]; then
    profile_response=$(curl -s -X GET "$BASE_URL/api/app/user/profile" \
      -H "Authorization: Bearer $access_token")
    
    if [[ $profile_response == *"success"*true* ]]; then
        echo "   ‚úÖ Perfil accesible con token v√°lido"
        
        # Extraer email del perfil
        profile_email=$(echo $profile_response | grep -o '"email":"[^"]*' | cut -d'"' -f4)
        echo "   üë§ Usuario: $profile_email"
    else
        echo "   ‚ùå Error accediendo al perfil"
        echo "   üìã Response: $profile_response"
    fi
