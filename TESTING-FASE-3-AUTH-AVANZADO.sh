    refresh_response=$(curl -s -X POST "$BASE_URL/api/app/auth/refresh" \
      -H "Content-Type: application/json" \
      -d "{\"refreshToken\": \"$refresh_token\"}")
    
    if [[ $refresh_response == *"accessToken"* ]]; then
        echo "   ‚úÖ Refresh token funcionando correctamente"
        
        # Extraer nuevo access token
        new_access_token=$(echo $refresh_response | grep -o '"accessToken":"[^"]*' | cut -d'"' -f4)
        echo "   üîë Nuevo access token: ${new_access_token:0:20}..."
        
        # Actualizar token para siguientes tests
        access_token=$new_access_token
    else
        echo "   ‚ùå Error con refresh token"
        echo "   üìã Response: $refresh_response"
    fi
else
    echo "   ‚ö†Ô∏è No se puede probar - no hay refresh token disponible"
fi

echo ""

# ===============================================
# TEST 5: SESSION INFO ENDPOINT
# ===============================================
echo "üîç TEST 5: Informaci√≥n de Sesi√≥n..."

if [[ -n "$access_token" ]]; then
    session_response=$(curl -s -X GET "$BASE_URL/api/app/auth/session-info" \
      -H "Authorization: Bearer $access_token")
    
    if [[ $session_response == *"sessionId"* ]]; then
        echo "   ‚úÖ Endpoint de sesi√≥n funcionando"
        
        # Extraer info de sesi√≥n
        user_role=$(echo $session_response | grep -o '"role":"[^"]*' | cut -d'"' -f4)
        session_id=$(echo $session_response | grep -o '"sessionId":"[^"]*' | cut -d'"' -f4)
        
        echo "   üë§ Rol: $user_role"
        echo "   üÜî Session ID: ${session_id:0:20}..."
    else
        echo "   ‚ùå Error obteniendo info de sesi√≥n"
        echo "   üìã Response: $session_response"
    fi
