# TEST 1: RATE LIMITING EN AUTH
# ===============================================
echo "üîç TEST 1: Rate Limiting en Authentication..."

echo "   üîÑ Enviando 10 requests r√°pidos a login..."
failed_attempts=0
for i in {1..10}; do
    response=$(curl -s -w "%{http_code}" -X POST "$BASE_URL/api/app/auth/login" \
      -H "Content-Type: application/json" \
      -d "{\"email\":\"fake@test.com\",\"password\":\"wrong\"}")
    
    http_code=$(echo "$response" | tail -c 4)
    
    if [[ "$http_code" == "429" ]]; then
        echo "   ‚úÖ Rate limit activado en intento $i (HTTP 429)"
        failed_attempts=$((failed_attempts + 1))
        break
    fi
done

