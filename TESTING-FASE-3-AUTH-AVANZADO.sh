    logout_response=$(curl -s -X POST "$BASE_URL/api/app/auth/logout" \
      -H "Authorization: Bearer $access_token")
    
    if [[ $logout_response == *"success"*true* ]]; then
        echo "   ‚úÖ Logout exitoso"
        
        # Verificar que el token ya no funciona
        verify_response=$(curl -s -X GET "$BASE_URL/api/app/user/profile" \
          -H "Authorization: Bearer $access_token")
        
        if [[ $verify_response == *"success"*false* ]] || [[ $verify_response == *"error"* ]]; then
            echo "   ‚úÖ Token invalidado correctamente despu√©s del logout"
        else
            echo "   ‚ö†Ô∏è Token sigue funcionando despu√©s del logout"
        fi
    else
        echo "   ‚ùå Error en logout"
        echo "   üìã Response: $logout_response"
    fi
else
    echo "   ‚ö†Ô∏è No se puede probar - no hay token disponible"
fi

echo ""

# ===============================================
# TEST 7: PROTECCI√ìN CONTRA FUERZA BRUTA
# ===============================================
echo "üîç TEST 7: Protecci√≥n contra Fuerza Bruta..."

echo "   üîÑ Simulando 6 intentos de login fallidos..."
brute_force_blocked=false

for i in {1..6}; do
    brute_response=$(curl -s -w "%{http_code}" -X POST "$BASE_URL/api/app/auth/login" \
      -H "Content-Type: application/json" \
      -d "{\"email\":\"brute@test.com\",\"password\":\"wrong$i\"}")
    
    http_code=$(echo "$brute_response" | tail -c 4)
    
    if [[ "$http_code" == "429" ]]; then
        echo "   ‚úÖ Protecci√≥n activada en intento $i (HTTP 429)"
        brute_force_blocked=true
        break
    fi
    
    # Peque√±a pausa para evitar race conditions
    sleep 0.1
done

if [[ $brute_force_blocked == true ]]; then
    echo "   ‚úÖ Protecci√≥n contra fuerza bruta funcionando"
else
    echo "   ‚ö†Ô∏è Protecci√≥n contra fuerza bruta no se activ√≥"
fi

echo ""

# ===============================================
# TEST 8: HEALTH CHECK MEJORADO
# ===============================================
echo "üîç TEST 8: Health Check con Informaci√≥n de Seguridad..."

health_response=$(curl -s "$BASE_URL/api/app/health")

if [[ $health_response == *"security"* ]]; then
    echo "   ‚úÖ Health check incluye informaci√≥n de seguridad"
    
    # Verificar caracter√≠sticas de seguridad
    if [[ $health_response == *"rateLimiting"* ]]; then
        echo "   ‚úÖ Rate limiting reportado como activo"
    fi
    
    if [[ $health_response == *"authentication"*"advanced"* ]]; then
        echo "   ‚úÖ Autenticaci√≥n avanzada reportada"
