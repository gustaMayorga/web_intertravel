    ((tests_passed++))
    echo "   ‚úÖ Test 7: Protecci√≥n Fuerza Bruta - PASSED"
else
    echo "   ‚ö†Ô∏è Test 7: Protecci√≥n Fuerza Bruta - PARTIAL"
